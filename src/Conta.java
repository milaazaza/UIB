
public class Conta {
	

	
	
	public int numero;
	public double saldo;
	public String dono;
	
	
	public int getNumero() {
		return numero;
	}
	
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	public double getSaldo() {
		return saldo;
	}
	
	public void setSaldo(double saldo) {
		this.saldo = saldo;
	}
	
	public String getDono() {
		return dono;
	}
	
	public void setDono (String dono) {
		this.dono = dono;
	}
	
	
	


}
